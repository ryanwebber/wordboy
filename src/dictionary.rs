use crate::utils::{AsciiChar, WordBuffer};

pub fn is_valid_guess(word: &WordBuffer) -> bool {
    if word.as_slice().iter().any(|&c| c == AsciiChar::NULL) {
        return false;
    }

    DICTIONARY.binary_search(word).is_ok()
}

